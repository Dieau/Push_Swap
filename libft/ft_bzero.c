#include "libft.h"

void	ft_bzero(void	*s, size_t n)
{
	int				i;
	unsigned char	*b;

	i = 0;
	b = (unsigned char *) s;
	while (n > 0)
	{
		b[i++] = 0;
		n--;
	}
}
